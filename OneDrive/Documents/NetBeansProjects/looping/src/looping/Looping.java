package looping;

public class Looping {

    public static void main(String[] args) {
        int i=5;
        while(i<=100)          
        {
            if(i>=20&&i<=35)
            {
                i+=5;
            continue;
        }
            System.out.println(i);
            if(i==75)
            {
                break;
            }
            i+=5;
            
        }
    }
}
        

     
        

        
        
                
        
                
    
